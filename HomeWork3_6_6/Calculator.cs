public static class Calculator {
    public static int Summate(int a, int b) {
        return a + b;
    }
    public static int Substract(int a, int b) {
        return a - b;
    }
    public static int Multiply(int a, int b) {
        return (a * b);
    }
    public static float Divide (float a, float b) {
        return (float)a / b;
    }

}
